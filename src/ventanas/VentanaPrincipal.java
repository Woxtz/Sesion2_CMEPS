    private void botonAceptarLetraAdivinadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarLetraAdivinadaActionPerformed
        // TODO add your handling code here:

        pasaTurno();

        for (int i = 0; i < nuevaPartida.getPalabraOculta().length(); i++) {
            palabraAdivinada += letras.get(i).getText();
        }

        if (nuevaPartida.finPartida()) {

            botonAceptarPalabraOculta.setEnabled(false);
            botonAceptarLetra.setEnabled(false);
            botonAñadirFallo.setEnabled(false);
            botonAceptarLetraAdivinada.setEnabled(false);

            botonSiguientePartida.setEnabled(true);
            if (ultimoAdivinador == 1) {

                aumentarPuntosJugador(1);

            } else {
                aumentarPuntosJugador(2);

            }

        } else {
            botonAceptarLetra.setEnabled(true);
        }

        txtPuntosJ1.setText(puntosJugador1 + "");
        txtPuntosJ2.setText(puntosJugador2 + "");
        botonAceptarLetraAdivinada.setEnabled(false);

        if (finJuego()) {
            
            
            botonReiniciarJuego.setEnabled(true);
            botonAceptarPalabraOculta.setEnabled(false);
            botonAceptarLetra.setEnabled(false);
            botonAñadirFallo.setEnabled(false);
            botonAceptarLetraAdivinada.setEnabled(false);
            botonSiguientePartida.setEnabled(false);
            

        }


    }//GEN-LAST:event_botonAceptarLetraAdivinadaActionPerformed

    private void botonAñadirFalloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAñadirFalloActionPerformed
        // TODO add your handling code here:
        pasaTurno();
        switch (nuevaPartida.getFallos()) {

            case 1:
                imagenLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/imagen0.png")));
                break;
            case 2:
                imagenLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/imagen1.png")));
                break;
            case 3:
                imagenLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/imagen 2.png")));
                break;
            case 4:
                imagenLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/imagen 3.png")));
                break;
            case 5:
                imagenLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/imagen 4.png")));
                break;
            case 6:
                imagenLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/imagen final.png")));

                if (ultimoAdivinador == 1) {

                    aumentarPuntosJugador(2);

                } else {
                    aumentarPuntosJugador(1);

                }

                txtPuntosJ1.setText(puntosJugador1 + "");
                txtPuntosJ2.setText(puntosJugador2 + "");
                break;

        }

        botonAceptarLetra.setEnabled(true);
        botonAñadirFallo.setEnabled(false);

        if (nuevaPartida.finPartida()) {
            botonAceptarPalabraOculta.setEnabled(false);
            botonAceptarLetra.setEnabled(false);
            botonAñadirFallo.setEnabled(false);
            botonAceptarLetraAdivinada.setEnabled(false);

            botonSiguientePartida.setEnabled(true);

        }

        if (finJuego()) {
            botonReiniciarJuego.setEnabled(true);
            botonAceptarPalabraOculta.setEnabled(false);
            botonAceptarLetra.setEnabled(false);
            botonAñadirFallo.setEnabled(false);
            botonAceptarLetraAdivinada.setEnabled(false);
            botonSiguientePartida.setEnabled(false);

        }
    }//GEN-LAST:event_botonAñadirFalloActionPerformed

