    public void reiniciarPartida() {
        nuevaPartida = new Partida();
        botonReiniciarJuego.setEnabled(false);
        botonAceptarLetra.setEnabled(false);
        botonAceptarLetraAdivinada.setEnabled(false);
        botonAceptarPalabraOculta.setEnabled(true);
        botonSiguientePartida.setEnabled(false);
        botonAñadirFallo.setEnabled(false);
        for (int i = 0; i < 10; i++) {
            letras.get(i).setText("");
            letras.get(i).setVisible(false);
            guiones.get(i).setVisible(false);
        }

        if (ultimoAdivinador == 1) {
            ultimoAdivinador = 2;
            turno = 1;
            txtAdivinador.setText("ADIVINADOR: JUGADOR 2");
        } else {
            ultimoAdivinador = 1;
            turno = 2;
            txtAdivinador.setText("ADIVINADOR: JUGADOR 1");
        }

        if (turno == 1) {
            txtTurnoJugadorX.setText("TURNO: JUGADOR 1");

        } else {
            txtTurnoJugadorX.setText("TURNO: JUGADOR 2");
        }
        
         
    }

    public void pasaTurno() {

        if (turno == 1) {
            turno = 2;
            txtTurnoJugadorX.setText("TURNO: JUGADOR 2");
            txtTurnoJugadorX.setForeground(Color.red);

        } else {
            turno = 1;
            txtTurnoJugadorX.setForeground(Color.blue);
            txtTurnoJugadorX.setText("TURNO: JUGADOR 1");
        }

    }

    public boolean finJuego() {
        if ((5 - puntosJugador1) == 2 || (5 - puntosJugador2) == 2) {
            if (puntosJugador1 > puntosJugador2) {
               
                txtGanador.setText("GANADOR JUGADOR 1");
                return true;
            } else {
               
                txtGanador.setText("GANADOR JUGADOR 2");
                return true;
            }
        } else {
            return false;
        }

    }

    public void aumentarPuntosJugador(int jugadorID) {
        if (jugadorID == 1) {
            this.puntosJugador1++;
        } else {
            this.puntosJugador2++;
        }
    }

